 * @method static \Illuminate\Database\Eloquent\Builder<static>|Product wherePrice($value)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|Product wherePurchasePrice($value)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|Product whereStockFollow($value)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|Product whereUpdatedAt($value)
 * @mixin \Eloquent
 */
class Product extends Model
{
    protected $guarded = ['id'];

    public function vendors()
    {
        return $this->belongsToMany(Vendor::class, 'vendor_products')
            ->withPivot('price')
            ->withTimestamps();
    }
}
